using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CubeCoordinates : MonoBehaviour
{
    public float x;
    public float y;
    public float z;

    public CubeCoordinates(float x, float y, float z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
